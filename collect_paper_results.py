        "the hyperparameter search / prior-baseline / isotonic-calibration changes -- they were "
        "written by an earlier modeling.ipynb execution, not the current model. Not recomputed "
        "here (would require the current fitted stacking model, which the headless pipeline "
        "(scripts/run_modeling_core.py) does not persist -- only modeling.ipynb saves it, and "
        "hasn't been re-executed since). Re-run modeling.ipynb against current outputs to get "
