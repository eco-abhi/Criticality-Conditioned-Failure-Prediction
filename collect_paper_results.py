def block_panel_logistic() -> dict:
    print("=== Block 4: Panel logistic regression ===")
    d = load_json(MODELING / "panel_significance_test.json", "scripts/panel_significance_test.py")
    if d is None:
        return null(f"{MODELING / 'panel_significance_test.json'} missing")

    lr = d["likelihood_ratio_test"]
    coefs = {row["feature"]: row for row in d["cluster_robust_coefficients"]}

    def odds_ratio(feat: str) -> dict:
        c = coefs.get(feat)
        if c is None:
            return null(f"feature {feat} not in cluster_robust_coefficients")
        est, se = c["coef"], c["cluster_robust_se"]
        return {
            "odds_ratio": r4(math.exp(est)),
            "ci95": [r4(math.exp(est - 1.96 * se)), r4(math.exp(est + 1.96 * se))],
        }

    return {
        "model_specification": (
            "Logistic regression (statsmodels GLM, Binomial family) with part-level CLUSTER-ROBUST "
            "(sandwich) standard errors -- NOT a random-intercept / mixed-effects GLMM (deliberately "
            "avoided: prone to convergence failure at this cluster count with an ~8% event rate). "
            "Outcome: compliance_failure. Fixed effects: full Layer 2 tabular feature set "
            "(get_layer2_tabular_features('clean')) plus crit_prob_A, crit_prob_B (crit_prob_C "
            "dropped as reference; the three sum to 1) and their supplier_at_risk_flag interactions "
            "(crit_prob_C's interaction dropped as reference for the same reason). Cluster variable: "
            "part_id."
        ),
        "criticality_coefficients": {
            "crit_prob_A": {
                "estimate": r4(coefs["crit_prob_A"]["coef"]),
                "cluster_robust_se": r4(coefs["crit_prob_A"]["cluster_robust_se"]),
                "z_stat": r4(coefs["crit_prob_A"]["coef"] / coefs["crit_prob_A"]["cluster_robust_se"]),
                "p_value_raw": fmt_p(coefs["crit_prob_A"]["cluster_robust_p_value"]),
                "odds_ratio": odds_ratio("crit_prob_A"),
            },
            "crit_prob_B": {
                "estimate": r4(coefs["crit_prob_B"]["coef"]),
                "cluster_robust_se": r4(coefs["crit_prob_B"]["cluster_robust_se"]),
                "z_stat": r4(coefs["crit_prob_B"]["coef"] / coefs["crit_prob_B"]["cluster_robust_se"]),
                "p_value_raw": fmt_p(coefs["crit_prob_B"]["cluster_robust_p_value"]),
                "odds_ratio": odds_ratio("crit_prob_B"),
            },
        },
        "omnibus_likelihood_ratio_test": {
            "description": "Tests whether the full crit_prob_* block (4 params: crit_prob_A, crit_prob_B, and their at_risk interactions) significantly improves fit over the tabular-only model.",
            "lr_statistic": r4(lr["lr_statistic"]),
            "df": lr["df"],
            "p_value_raw": fmt_p(lr["p_value"]),
            "significant_at_0.05": bool(lr["p_value"] < 0.05),
            "significant_at_0.01": bool(lr["p_value"] < 0.01),
        },
        "n_observations": d["n_rows"],
        "n_parts_clusters": d["n_parts_clusters"],
        "n_events": d["n_events"],
        "n_months": null("Not directly recorded in panel_significance_test.json; the compliance panel is part_month grain with N_MONTHS=24 by generator construction, but the exact realized month count per part-cluster in this scope was not persisted."),
        "convergence_notes": d["caveat"],
        "freshness_note": (
            "This result was regenerated immediately before collect_paper_results.py ran, because "
            "the on-disk copy was stale (computed against the pre-hyperparameter-search GAT model). "
            "IMPORTANT: with the pre-search model, this LR test was p=0.022 (significant); with the "
            "post-search model, it is now the value reported above. This is a real, substantive "
            "change from re-tuning, not noise -- see metadata.notes."
        ),
    }


