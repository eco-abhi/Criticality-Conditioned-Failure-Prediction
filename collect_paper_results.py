"""
Collect existing pipeline outputs into outputs/paper_results.json and
outputs/paper_results_summary.txt for the paper's results section.

Loads existing outputs only -- does not retrain or refit anything (the one exception, done
BEFORE this script runs, not inside it: panel_significance_test.json was regenerated because it
had gone stale relative to the post-hyperparameter-search model -- a fast logistic-regression
refit against already-trained model outputs, not a retrain). If a referenced file is missing,
prints a clear error naming the file and the script that generates it, then writes null with a
companion "_note" field for that entry rather than crashing.

Run: uv run python collect_paper_results.py
"""
