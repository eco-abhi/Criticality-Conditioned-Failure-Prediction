def find_missing(d: Any, prefix: str = "") -> list[str]:
    found = []
    if isinstance(d, dict):
        if "_note" in d and "value" in d and d.get("value") is None and len(d) == 2:
            found.append(prefix or "(root)")
            return found
        for k, v in d.items():
            found.extend(find_missing(v, f"{prefix}.{k}" if prefix else k))
    return found


