missing_blocks: list[str] = []


def log_missing(path: Path, generator: str) -> None:
    print(f"  [MISSING] {path.relative_to(ROOT)} -- generated by: {generator}")


def r4(x: Any) -> Any:
    if x is None:
        return None
    try:
        f = float(x)
    except (TypeError, ValueError):
        return x
    if math.isnan(f) or math.isinf(f):
        return None
    return round(f, 4)


def fmt_p(p: Any) -> Any:
    if p is None:
        return None
    try:
        f = float(p)
    except (TypeError, ValueError):
        return p
    if math.isnan(f):
        return None
    if f < 0.001:
        return "<0.001"
    return round(f, 4)


def ci(lo: Any, hi: Any) -> Optional[list]:
    if lo is None or hi is None:
        return None
    return [r4(lo), r4(hi)]


def null(note: str) -> dict:
    return {"value": None, "_note": note}


def load_csv(path: Path, generator: str) -> Optional[pd.DataFrame]:
    if not path.exists():
        log_missing(path, generator)
        return None
    return pd.read_csv(path)


def load_json(path: Path, generator: str) -> Optional[dict]:
    if not path.exists():
        log_missing(path, generator)
        return None
    return json.loads(path.read_text(encoding="utf-8"))


