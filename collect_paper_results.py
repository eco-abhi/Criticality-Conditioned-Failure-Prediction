def block_multiseed() -> dict:
    print("=== Block 3: Multi-seed study (fast mode) ===")
    summary = load_csv(OUT / "multi_seed" / "summary.csv", "scripts/multi_seed_significance.py")
    sig = load_csv(OUT / "multi_seed" / "significance.csv", "scripts/multi_seed_significance.py")

    if summary is None or sig is None:
        return null("outputs/multi_seed/{summary,significance}.csv missing -- run scripts/multi_seed_significance.py")

    n_seeds = len(summary)
    out: dict = {"n_seeds": n_seeds, "comparisons": {}}
    for comp in ["conditioned_vs_uniform", "oracle_vs_uniform"]:
        out["comparisons"][comp] = {}
        for metric in ["auc_pr", "f1", "brier"]:
            rows = sig[(sig["comparison"] == comp) & (sig["metric"] == metric)]
            if len(rows) == 0:
                continue
            row = rows.iloc[0]
            out["comparisons"][comp][metric] = {
                "mean_diff": r4(row["mean_diff"]),
                "ci95": ci(row["ci95_low"], row["ci95_high"]),
                "p_value_raw": fmt_p(row["p_value_paired_t"]),
                "p_value_fdr_bh": fmt_p(row["p_value_fdr_bh"]),
            }

    n_cond_beats_uni = int((summary["conditioned_auc_pr"] > summary["uniform_auc_pr"]).sum())
    n_orac_beats_uni = int((summary["oracle_auc_pr"] > summary["uniform_auc_pr"]).sum())
    out["directional_consistency"] = {
        "conditioned_beats_uniform_auc_pr": f"{n_cond_beats_uni}/{n_seeds}",
        "oracle_beats_uniform_auc_pr": f"{n_orac_beats_uni}/{n_seeds}",
    }
    return out


