# ---------------------------------------------------------------------------
COST_A_FAILURE = 50000
COST_B_FAILURE = 10000
COST_C_FAILURE = 2000
COST_FALSE_POSITIVE = 500
PLANNING_HORIZON_MONTHS = 24


def block_business_value() -> dict:
    print("=== Block 8: Business value simulation (synthetic only) ===")
    bv = load_csv(MODELING / "business_value_simulation.csv", "scripts/run_modeling_core.py (business_value_summary, model=conditioned)")
    biz_thr = load_csv(MODELING / "compliance_comparison_business_thresholds.csv", "scripts/run_modeling_core.py (metrics_at_fixed_thresholds)")
    if bv is None:
        return null(f"{MODELING / 'business_value_simulation.csv'} missing")

    cost_by = {"A": COST_A_FAILURE, "B": COST_B_FAILURE, "C": COST_C_FAILURE}
    out: dict = {
        "cost_assumptions": {
            "cost_A_failure": COST_A_FAILURE, "cost_B_failure": COST_B_FAILURE,
            "cost_C_failure": COST_C_FAILURE, "cost_false_positive": COST_FALSE_POSITIVE,
            "planning_horizon_months": PLANNING_HORIZON_MONTHS,
        },
        "note": "Synthetic data only -- no real-data business value simulation exists. Model = conditioned (business_value_summary is only computed for the conditioned model in run_layer2_evaluation).",
        "by_threshold": {},
    }
    prec_rec_by_thr = {}
    if biz_thr is not None:
        cond_rows = biz_thr[biz_thr["model"] == "conditioned"]
        for _, row in cond_rows.iterrows():
            prec_rec_by_thr[float(row["threshold"])] = {"precision": r4(row["precision"]), "recall": r4(row["recall"])}

    for _, row in bv.iterrows():
        thr = float(row["threshold"])
        cost_avoided = {c: r4(row[f"tp_{c}"] * cost_by[c]) for c in "ABC"}
        entry = {
            "true_positives_by_class": {c: int(row[f"tp_{c}"]) for c in "ABC"},
            "false_positives_by_class": {c: int(row[f"fp_{c}"]) for c in "ABC"},
            "false_negatives_by_class": {c: int(row[f"fn_{c}"]) for c in "ABC"},
            "cost_avoided_by_class": cost_avoided,
            "cost_of_false_positives": r4(row["fp_cost"]),
            "net_value": r4(row["net_value"]),
            "precision_recall": prec_rec_by_thr.get(thr, null("compliance_comparison_business_thresholds.csv missing this threshold for model=conditioned")),
        }
        # Derived per-class net value: (tp+fn)*cost_class [baseline: all actual failures cost] -
        # fn*cost_class [missed, still costs] - fp*FP_COST [false alarms] = tp*cost_class - fp*FP_COST
        entry["net_value_by_class_derived"] = {
            c: r4(row[f"tp_{c}"] * cost_by[c] - row[f"fp_{c}"] * COST_FALSE_POSITIVE) for c in "ABC"
        }
        out["by_threshold"][str(thr)] = entry

    best_overall = max(out["by_threshold"].items(), key=lambda kv: kv[1]["net_value"])[0]
    best_a = max(out["by_threshold"].items(), key=lambda kv: kv[1]["net_value_by_class_derived"]["A"])[0]
    out["threshold_maximizing_net_value_overall"] = best_overall
    out["threshold_maximizing_net_value_A_parts"] = best_a
    return out


