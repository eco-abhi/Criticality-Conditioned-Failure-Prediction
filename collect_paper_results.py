# Block 5: Oracle ceiling (synthetic data) -- derived from Block 2's source files
# ---------------------------------------------------------------------------
def block_oracle_ceiling() -> dict:
    print("=== Block 5: Oracle ceiling (synthetic) ===")
    cmp = load_csv(MODELING / "compliance_comparison.csv", "scripts/run_modeling_core.py")
    oracle_j = load_json(MODELING / "layer2_oracle_true_class.json", "scripts/run_modeling_core.py")
    if cmp is None:
        return null(f"{MODELING / 'compliance_comparison.csv'} missing")

    rows = {r["model"]: r for _, r in cmp.iterrows()}
    out = {}
    for key in ["uniform", "conditioned", "oracle"]:
        if key not in rows:
            out[key] = null(f"model={key} row missing from compliance_comparison.csv")
            continue
        out[key] = {m: r4(rows[key][m]) for m in ["auc_roc", "auc_pr", "brier"]}
        if key == "oracle" and oracle_j is not None:
            out[key]["by_class_brier"] = {c: r4(oracle_j["by_criticality_true"].get(c, {}).get("brier")) for c in "ABC"}

    gap_cond = None
    gap_uni = None
    if "oracle" in rows and "conditioned" in rows:
        gap_cond = float(rows["oracle"]["auc_pr"]) - float(rows["conditioned"]["auc_pr"])
    if "oracle" in rows and "uniform" in rows:
        gap_uni = float(rows["oracle"]["auc_pr"]) - float(rows["uniform"]["auc_pr"])

    interpretation = (
        f"Oracle AUC-PR exceeds conditioned by {r4(gap_cond)} and uniform by {r4(gap_uni)}; "
        "since Layer 1's predicted criticality falls short of the true-class oracle, there is "
        "measurable headroom -- improving Layer 1's classification accuracy is a more promising "
        "lever than further tuning Layer 2's conditioning logic alone."
        if gap_cond is not None and gap_uni is not None
        else "Gap not computed -- see missing model rows above."
    )

    out["auc_pr_gap_oracle_minus_conditioned"] = r4(gap_cond)
    out["auc_pr_gap_oracle_minus_uniform"] = r4(gap_uni)
    out["interpretation"] = interpretation
    return out


