def block_layer2_predictive() -> dict:
    print("=== Block 2: Layer 2 predictive (single full run) ===")
    cmp = load_csv(MODELING / "compliance_comparison.csv", "scripts/run_modeling_core.py")
    boot = load_csv(MODELING / "compliance_comparison_bootstrap.csv", "scripts/run_modeling_core.py (bootstrap_layer2_comparisons)")
    j = {
        "prior_baseline": load_json(MODELING / "layer2_baseline_marginal_prior.json", "scripts/run_modeling_core.py"),
        "conditioned": load_json(MODELING / "layer2_full_conditioned.json", "scripts/run_modeling_core.py"),
        "oracle": load_json(MODELING / "layer2_oracle_true_class.json", "scripts/run_modeling_core.py"),
    }

    models: dict = {}
    if cmp is not None:
        for _, row in cmp.iterrows():
            if row["model"] not in ("prior", "conditioned", "oracle"):
                continue
            key = "prior_baseline" if row["model"] == "prior" else row["model"]
            models[key] = {m: r4(row[m]) for m in ["auc_roc", "auc_pr", "f1", "precision", "recall", "brier"]}
            models[key]["bootstrap_ci95"] = null("compliance_comparison.csv has point estimates only; CIs are in compliance_comparison_bootstrap.csv as pairwise diffs, not per-model absolute CIs.")
            src = j.get(key)
            models[key]["by_class"] = (
                {c: {"auc_roc": r4(src["by_criticality_true"].get(c, {}).get("auc_roc")),
                     "auc_pr": r4(src["by_criticality_true"].get(c, {}).get("auc_pr")),
                     "brier": r4(src["by_criticality_true"].get(c, {}).get("brier"))}
                 for c in "ABC"}
                if src else null(f"layer2_{'baseline_marginal_prior' if key=='prior_baseline' else key if key=='conditioned' else 'oracle_true_class'}.json missing")
            )
    else:
        models = null(f"{MODELING / 'compliance_comparison.csv'} missing")

    sig: dict = {}
    if boot is not None:
        for _, row in boot.iterrows():
            comp = row["comparison"]
            if comp not in ("conditioned_vs_prior", "oracle_vs_prior", "oracle_vs_conditioned"):
                continue
            sig.setdefault(comp, {})[row["metric"]] = {
                "point_diff": r4(row["point_diff"]),
                "ci95": ci(row["ci_low"], row["ci_high"]),
                "p_value_raw": fmt_p(row["p_value_two_sided"]),
                "p_value_fdr_bh": fmt_p(row["p_value_fdr_bh"]),
            }
    else:
        sig = null(f"{MODELING / 'compliance_comparison_bootstrap.csv'} missing")

    return {"models": models, "pairwise_significance_fdr": sig}


